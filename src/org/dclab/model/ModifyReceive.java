package org.dclab.model;

public class ModifyReceive {
	private int roleId;
	private String roleName;
	private int createModel;
	private int viewModel;
	private int verifyModel;
	private int manage;
	private int vote;
	public int getRoleId() {
		return roleId;
	}
	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public int getCreateModel() {
		return createModel;
	}
	public void setCreateModel(int createModel) {
		this.createModel = createModel;
	}
	public int getViewModel() {
		return viewModel;
	}
	public void setViewModel(int viewModel) {
		this.viewModel = viewModel;
	}
	public int getVerifyModel() {
		return verifyModel;
	}
	public void setVerifyModel(int verifyModel) {
		this.verifyModel = verifyModel;
	}
	public int getManage() {
		return manage;
	}
	public void setManage(int manage) {
		this.manage = manage;
	}
	public int getVote() {
		return vote;
	}
	public void setVote(int vote) {
		this.vote = vote;
	}
	
	
	
}
